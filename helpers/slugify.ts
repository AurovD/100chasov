const alphabet: Record<string, string> = {
    "а": "a",
    "б": "b",
    "в": "v",
    "г": "g",
    "д": "d",
    "е": "e",
    "ё": "yo",
    "ж": "zh",
    "з": "z",
    "и": "i",
    "й": "y",
    "к": "k",
    "л": "l",
    "м": "m",
    "н": "n",
    "о": "o",
    "п": "p",
    "р": "r",
    "с": "s",
    "т": "t",
    "у": "u",
    "ф": "f",
    "х": "h",
    "ц": "ts",
    "ч": "ch",
    "ш": "sh",
    "щ": "sch",
    "ъ": "",
    "ы": "y",
    "ь": "",
    "э": "e",
    "ю": "yu",
    "я": "ya",
    " ": "-"
};


export function slugify(text: string): string {
    return text.toLocaleLowerCase().split("").map((symbol: string) => {
        if(Object.hasOwn(alphabet, symbol)) {
            return alphabet[symbol];
        } else if(symbol.match(/^[a-z0-9]+$/i)) {
            return symbol;
        }
        return "";
    }).join("");
}